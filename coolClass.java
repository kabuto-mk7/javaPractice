public class coolClass {

    public void doStuff(){
        // making a logic method within an external class file

        // if statement
        int a = 5;
        if(a == 0){
            System.out.println("a is 0");
        }else if(a == 1){
            System.out.println("a is 1");
        }else{
            System.out.println("a is not 0 or 1");
        }
    }

    public void doStuffFor(){

        //for loop
        for(int i = 0; i < 8; i++){
            System.out.println("This prints 8 times");
        }
    }

    public void doStuffWhile(){
        int a = 5;
        //while statement
        while(a==50){
            System.out.println("a is 5");
            a++;
        }
    }
}
